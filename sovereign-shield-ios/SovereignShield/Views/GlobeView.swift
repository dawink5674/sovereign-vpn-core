import SwiftUI
import SceneKit

struct GlobeView: View {
    @ObservedObject var vpnManager = VPNManager.shared
    @State private var userLocation: LocationInfo?
    @State private var serverLocation: LocationInfo?

    var body: some View {
        ZStack {
            Theme.spaceBlack.ignoresSafeArea()

            VStack(spacing: 0) {
                // Title
                Text("THREAT MAP")
                    .font(.system(size: 18, weight: .bold))
                    .tracking(3)
                    .foregroundColor(Theme.textBright)
                    .padding(.top, 16)

                // 3D Globe
                SceneKitGlobeView(
                    userLocation: userLocation,
                    serverLocation: vpnManager.connectionState == .connected ? serverLocation : nil,
                    isConnected: vpnManager.connectionState == .connected
                )
                .frame(maxWidth: .infinity)
                .frame(height: 400)
                .padding(.horizontal, 8)

                // Location badges
                locationBadges

                // Connection info
                if vpnManager.connectionState == .connected {
                    connectedOverlay
                } else {
                    disconnectedOverlay
                }

                // Protocol info bar
                protocolInfoBar

                Spacer()
            }
        }
        .task {
            userLocation = await GeoIpClient.shared.fetchCurrentLocation()
            if vpnManager.connectionState == .connected {
                serverLocation = await GeoIpClient.shared.fetchLocationForIP("35.206.67.49")
            }
        }
    }

    // MARK: - Location Badges

    private var locationBadges: some View {
        HStack(spacing: 16) {
            if let loc = userLocation {
                locationBadge(
                    label: "YOU",
                    city: loc.city,
                    region: loc.region,
                    color: Theme.shieldCyan
                )
            }

            if vpnManager.connectionState == .connected, let loc = serverLocation {
                locationBadge(
                    label: "VPN",
                    city: loc.city,
                    region: loc.region,
                    color: Theme.statusConnected
                )
            }
        }
        .padding(.horizontal, 20)
    }

    private func locationBadge(label: String, city: String, region: String, color: Color) -> some View {
        HStack(spacing: 8) {
            Circle()
                .fill(color)
                .frame(width: 6, height: 6)
            VStack(alignment: .leading, spacing: 1) {
                Text(label)
                    .font(.system(size: 9, weight: .bold))
                    .tracking(1)
                    .foregroundColor(color)
                Text("\(city), \(region)")
                    .font(.system(size: 11))
                    .foregroundColor(Theme.textSecondary)
                    .lineLimit(1)
            }
        }
        .padding(.horizontal, 12)
        .padding(.vertical, 6)
        .background(
            RoundedRectangle(cornerRadius: 8)
                .fill(Theme.spaceCard.opacity(0.8))
        )
        .overlay(
            RoundedRectangle(cornerRadius: 8)
                .stroke(color.opacity(0.2), lineWidth: 1)
        )
    }

    // MARK: - Overlays

    private var connectedOverlay: some View {
        HStack(spacing: 8) {
            Circle()
                .fill(Theme.statusConnected)
                .frame(width: 8, height: 8)
            Text("ENCRYPTED TUNNEL ACTIVE")
                .font(.system(size: 11, weight: .bold))
                .tracking(1.5)
                .foregroundColor(Theme.statusConnected)
        }
        .padding(.vertical, 8)
    }

    private var disconnectedOverlay: some View {
        Text("CONNECT TO ACTIVATE TUNNEL")
            .font(.system(size: 11, weight: .medium))
            .tracking(1)
            .foregroundColor(Theme.textMuted)
            .padding(.vertical, 8)
    }

    // MARK: - Protocol Info Bar

    private var protocolInfoBar: some View {
        HStack(spacing: 20) {
            protocolItem("Protocol", "WireGuard")
            protocolItem("Encryption", "ChaCha20")
            protocolItem("Key Exchange", "Curve25519")
        }
        .padding(.horizontal, 20)
        .padding(.vertical, 8)
    }

    private func protocolItem(_ label: String, _ value: String) -> some View {
        VStack(spacing: 2) {
            Text(label)
                .font(.system(size: 8, weight: .bold))
                .tracking(0.5)
                .foregroundColor(Theme.textMuted)
            Text(value)
                .font(.system(size: 10, weight: .medium))
                .foregroundColor(Theme.textSecondary)
        }
    }
}

// MARK: - SceneKit Globe

struct SceneKitGlobeView: UIViewRepresentable {
    let userLocation: LocationInfo?
    let serverLocation: LocationInfo?
    let isConnected: Bool

    func makeUIView(context: Context) -> SCNView {
        let sceneView = SCNView()
        sceneView.backgroundColor = UIColor(Theme.spaceBlack)
        sceneView.allowsCameraControl = true
        sceneView.antialiasingMode = .multisampling4X
        sceneView.autoenablesDefaultLighting = false

        let scene = SCNScene()
        sceneView.scene = scene

        // Camera
        let cameraNode = SCNNode()
        cameraNode.camera = SCNCamera()
        cameraNode.camera?.fieldOfView = 45
        cameraNode.position = SCNVector3(0, 0, 4.5)
        scene.rootNode.addChildNode(cameraNode)

        // Ambient light
        let ambientLight = SCNNode()
        ambientLight.light = SCNLight()
        ambientLight.light?.type = .ambient
        ambientLight.light?.color = UIColor(white: 0.3, alpha: 1)
        scene.rootNode.addChildNode(ambientLight)

        // Directional light
        let dirLight = SCNNode()
        dirLight.light = SCNLight()
        dirLight.light?.type = .directional
        dirLight.light?.color = UIColor(Theme.shieldBlueDim).withAlphaComponent(0.5)
        dirLight.position = SCNVector3(5, 5, 5)
        dirLight.look(at: SCNVector3Zero)
        scene.rootNode.addChildNode(dirLight)

        // Globe sphere
        let globeNode = createGlobe()
        scene.rootNode.addChildNode(globeNode)

        // Grid lines
        let gridNode = createGridLines()
        globeNode.addChildNode(gridNode)

        // Continent outlines
        let continentNode = createContinentOutlines()
        globeNode.addChildNode(continentNode)

        // Atmosphere glow
        let atmosphereNode = createAtmosphere()
        scene.rootNode.addChildNode(atmosphereNode)

        // Rotation animation (120 seconds per revolution)
        let rotation = SCNAction.repeatForever(
            SCNAction.rotateBy(x: 0, y: .pi * 2, z: 0, duration: 120)
        )
        globeNode.runAction(rotation, forKey: "rotation")

        // User location pin
        if let loc = userLocation {
            let pinNode = createLocationPin(
                latitude: loc.latitude,
                longitude: loc.longitude,
                color: UIColor(Theme.shieldCyan)
            )
            globeNode.addChildNode(pinNode)
        }

        // Server location pin
        if isConnected, let loc = serverLocation {
            let pinNode = createLocationPin(
                latitude: loc.latitude,
                longitude: loc.longitude,
                color: UIColor(Theme.statusConnected)
            )
            globeNode.addChildNode(pinNode)

            // Arc between user and server
            if let userLoc = userLocation {
                let arcNode = createArc(
                    from: (userLoc.latitude, userLoc.longitude),
                    to: (loc.latitude, loc.longitude)
                )
                globeNode.addChildNode(arcNode)
            }
        }

        context.coordinator.globeNode = globeNode
        return sceneView
    }

    func updateUIView(_ uiView: SCNView, context: Context) {
        // Update pins on state change
    }

    func makeCoordinator() -> Coordinator {
        Coordinator()
    }

    class Coordinator {
        var globeNode: SCNNode?
    }

    // MARK: - Globe Creation

    private func createGlobe() -> SCNNode {
        let sphere = SCNSphere(radius: 1.5)
        sphere.segmentCount = 64

        let material = SCNMaterial()
        material.diffuse.contents = UIColor(Theme.spaceDark)
        material.specular.contents = UIColor(Theme.shieldBlueDim).withAlphaComponent(0.3)
        material.shininess = 0.2
        material.transparency = 0.95
        sphere.materials = [material]

        let node = SCNNode(geometry: sphere)
        return node
    }

    // MARK: - Grid Lines

    private func createGridLines() -> SCNNode {
        let gridRoot = SCNNode()
        let radius: Float = 1.502

        // Latitude lines every 30 degrees
        for lat in stride(from: -60, through: 60, by: 30) {
            let latRad = Float(lat) * .pi / 180
            let r = radius * cos(latRad)
            let y = radius * sin(latRad)

            let torus = SCNTorus(ringRadius: CGFloat(r), pipeRadius: 0.002)
            let material = SCNMaterial()
            material.diffuse.contents = UIColor(Theme.chartGrid).withAlphaComponent(0.3)
            material.isDoubleSided = true
            torus.materials = [material]

            let node = SCNNode(geometry: torus)
            node.position = SCNVector3(0, y, 0)
            gridRoot.addChildNode(node)
        }

        // Longitude lines every 30 degrees
        for lon in stride(from: 0, through: 150, by: 30) {
            let lonRad = Float(lon) * .pi / 180

            var vertices: [SCNVector3] = []
            for i in stride(from: 0, through: 360, by: 5) {
                let angle = Float(i) * .pi / 180
                let x = radius * cos(angle) * sin(lonRad)
                let y = radius * sin(angle)
                let z = radius * cos(angle) * cos(lonRad)
                vertices.append(SCNVector3(x, y, z))
            }

            let lineNode = createLineNode(vertices: vertices, color: UIColor(Theme.chartGrid).withAlphaComponent(0.3))
            gridRoot.addChildNode(lineNode)
        }

        return gridRoot
    }

    // MARK: - Continent Outlines

    private func createContinentOutlines() -> SCNNode {
        let continentRoot = SCNNode()
        let radius: Float = 1.505

        for polygon in ContinentData.allOutlines {
            var vertices: [SCNVector3] = []

            for i in stride(from: 0, to: polygon.count - 1, by: 2) {
                let lat = polygon[i]
                let lon = polygon[i + 1]
                let pos = latLonToPosition(lat: lat, lon: lon, radius: radius)
                vertices.append(pos)
            }

            if vertices.count >= 2 {
                let lineNode = createLineNode(
                    vertices: vertices,
                    color: UIColor(Theme.shieldCyan).withAlphaComponent(0.6)
                )
                continentRoot.addChildNode(lineNode)
            }
        }

        return continentRoot
    }

    // MARK: - Atmosphere

    private func createAtmosphere() -> SCNNode {
        let sphere = SCNSphere(radius: 1.9)
        sphere.segmentCount = 48

        let material = SCNMaterial()
        material.diffuse.contents = UIColor(Theme.shieldBlue).withAlphaComponent(0.06)
        material.isDoubleSided = true
        material.writesToDepthBuffer = false
        material.blendMode = .add
        sphere.materials = [material]

        return SCNNode(geometry: sphere)
    }

    // MARK: - Location Pin

    private func createLocationPin(latitude: Double, longitude: Double, color: UIColor) -> SCNNode {
        let pinRoot = SCNNode()

        // Pin sphere
        let pinSphere = SCNSphere(radius: 0.03)
        let material = SCNMaterial()
        material.diffuse.contents = color
        material.emission.contents = color
        pinSphere.materials = [material]

        let pinNode = SCNNode(geometry: pinSphere)
        let pos = latLonToPosition(lat: Float(latitude), lon: Float(longitude), radius: 1.52)
        pinNode.position = pos
        pinRoot.addChildNode(pinNode)

        // Pulse ring
        let ring = SCNTorus(ringRadius: 0.06, pipeRadius: 0.005)
        let ringMaterial = SCNMaterial()
        ringMaterial.diffuse.contents = color.withAlphaComponent(0.5)
        ringMaterial.emission.contents = color.withAlphaComponent(0.3)
        ring.materials = [ringMaterial]

        let ringNode = SCNNode(geometry: ring)
        ringNode.position = pos

        // Orient ring to face outward
        let normal = SCNVector3(pos.x, pos.y, pos.z)
        let up = SCNVector3(0, 1, 0)
        ringNode.look(at: SCNVector3(pos.x * 2, pos.y * 2, pos.z * 2), up: up, localFront: SCNVector3(0, 1, 0))
        pinRoot.addChildNode(ringNode)

        // Pulse animation
        let scaleUp = SCNAction.scale(to: 1.5, duration: 1.0)
        let scaleDown = SCNAction.scale(to: 1.0, duration: 1.0)
        let fadeOut = SCNAction.fadeOpacity(to: 0.3, duration: 1.0)
        let fadeIn = SCNAction.fadeOpacity(to: 1.0, duration: 1.0)
        let pulse = SCNAction.repeatForever(
            SCNAction.sequence([
                SCNAction.group([scaleUp, fadeOut]),
                SCNAction.group([scaleDown, fadeIn])
            ])
        )
        ringNode.runAction(pulse)

        return pinRoot
    }

    // MARK: - Arc

    private func createArc(from start: (Double, Double), to end: (Double, Double)) -> SCNNode {
        let segments = 50
        var vertices: [SCNVector3] = []

        for i in 0...segments {
            let t = Float(i) / Float(segments)
            let lat = Float(start.0) + t * Float(end.0 - start.0)
            let lon = Float(start.1) + t * Float(end.1 - start.1)
            // Add arc height
            let height: Float = 1.55 + 0.15 * sin(.pi * t)
            let pos = latLonToPosition(lat: lat, lon: lon, radius: height)
            vertices.append(pos)
        }

        return createLineNode(vertices: vertices, color: UIColor(Theme.statusConnected).withAlphaComponent(0.7))
    }

    // MARK: - Helpers

    private func latLonToPosition(lat: Float, lon: Float, radius: Float) -> SCNVector3 {
        let latRad = lat * .pi / 180
        let lonRad = lon * .pi / 180
        let x = radius * cos(latRad) * sin(lonRad)
        let y = radius * sin(latRad)
        let z = radius * cos(latRad) * cos(lonRad)
        return SCNVector3(x, y, -z)
    }

    private func createLineNode(vertices: [SCNVector3], color: UIColor) -> SCNNode {
        guard vertices.count >= 2 else { return SCNNode() }

        var indices: [Int32] = []
        for i in 0..<(vertices.count - 1) {
            indices.append(Int32(i))
            indices.append(Int32(i + 1))
        }

        let vertexSource = SCNGeometrySource(vertices: vertices)
        let indexData = Data(bytes: indices, count: indices.count * MemoryLayout<Int32>.size)
        let element = SCNGeometryElement(
            data: indexData,
            primitiveType: .line,
            primitiveCount: indices.count / 2,
            bytesPerIndex: MemoryLayout<Int32>.size
        )

        let geometry = SCNGeometry(sources: [vertexSource], elements: [element])
        let material = SCNMaterial()
        material.diffuse.contents = color
        material.emission.contents = color
        material.isDoubleSided = true
        geometry.materials = [material]

        return SCNNode(geometry: geometry)
    }
}

